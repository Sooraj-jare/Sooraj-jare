package com.wipro.messagePublisher.pojo;

public class ExchangeMessage {
	private String deliveringAgent;

	private String seller;

	private String safekeepingAccount;

	private String placeOfSettlement;

	private String placeOfSafekeepingAccount;

	public String getDeliveringAgent() {
		return deliveringAgent;
	}

	public void setDeliveringAgent(String deliveringAgent) {
		this.deliveringAgent = deliveringAgent;
	}

	public String getSeller() {
		return seller;
	}

	public void setSeller(String seller) {
		this.seller = seller;
	}

	public String getSafekeepingAccount() {
		return safekeepingAccount;
	}

	public void setSafekeepingAccount(String safekeepingAccount) {
		this.safekeepingAccount = safekeepingAccount;
	}

	public String getPlaceOfSettlement() {
		return placeOfSettlement;
	}

	public void setPlaceOfSettlement(String placeOfSettlement) {
		this.placeOfSettlement = placeOfSettlement;
	}

	public String getPlaceOfSafekeepingAccount() {
		return placeOfSafekeepingAccount;
	}

	public void setPlaceOfSafekeepingAccount(String placeOfSafekeepingAccount) {
		this.placeOfSafekeepingAccount = placeOfSafekeepingAccount;
	}
	
	
	
}
